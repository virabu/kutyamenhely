        <p>In het begin...<br>
        Wij begonnen onze dieren beschermingsactiviteit in Juli 1997 door ons bij Herosz aan te sluiten - de Hongaarse Nationale Maatschappij voor de Bescherming van dier en natuur. Op dit moment, bleek de taak om verdwaalde honden te verzamelen, hen te voorzien van een tijdelijke schuilplaats, indien mogelijk hen terug te plaatsen bij hun eigenaars en anders een nieuw onderkomen voor hun te vinden een belangrijk probleem in de stad Szekszárd. Door een overeenkomst tussen het bureau van de burgemeester en ons, creëerden wij een dierenopvang op het terein van het stadspond. De eerste dakloze honden kwamen in September 1997 aan.</p>
        <p>Onze Activiteiten<br>
        In 1998 namen wij dieren rechten van het bureau van de burgemeester over. In Mei 1998 vestigden wij de Stichting van de Provincie Tolna voor de Bescherming van dier en natuur. Sindsdien zijn wij verder gegaan met: De dierenopvang in werking te stellen in Szekszard. Verdwaalde dieren te verzamelen, opvang en zorg verstrekken voor hen, hen terug plaatsen bij hun eigenaars, of, als niemand hen terugbvraagt, proberen een nieuw onderkomen voor hen te vinden. Het organiseren van dierlijke zorgprogramma's zoals: tentoonstellingen, prestaties van hondscholen, kampeerweken voor kinderen, familieprogramma's, enz. Wij hebben zeer goede relaties met de lokale media. Door deze kanalen, proberen wij om een menselijke houding ten opzichte van dieren te bevorderen en de vreselijke situatie wat betreft dierenbescherming in Hongarije te verbeteren. Het werk van dag tot dag wordt uitgevoerd door tien mensen. Het aantal van onze ondersteunende leden is bijna 100.</p>
        <p>De Nieuwe Schuilplaats<br>
        Na veel werk van zowel het organiseren als het uitvoeren, kon de stichting onze nieuwe, modernere opvang op 23 Juni, 2000 openen. De opvang heeft alle noodzakelijke certificaten van de autoriteiten en is met de steun van het Bureau van de Burgemeester Szekszard in werking gesteld. In 2001, won onze schuilplaats de "Schuilplaats van het Jaar" prijs, die door de Nationale Vereniging van Hongaarse Journalisten wordt toegekend.</p>
