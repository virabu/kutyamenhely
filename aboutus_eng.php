        <p>We started our animal protection activity in July 1997 by joining Herosz - The Hungarian Nation-wide Society for the Protection of Animals and Nature. At this time, the task of collecting stray dogs, providing them with temporary shelter, returning them to their owners if possible, otherwise finding new homes for them constituted a major problem in the town of Szekszard. Therefore, by an agreement between the mayor's office and us, we created an animal shelter on the premises of the city pound. The first homeless dogs arrived to the shelter in September 1997.</p>
        <p>In 1998 we took over animal duties from the mayor's office. In May 1998 we established the Tolna County Foundation for the Protection of Animals and Nature. Since then, we have gone on to: Run the Szekszard Animal Shelter. Collect stray animals, provide home and care for them, give them back to their owners, or, if nobody reclaims them, try to find a new home for them. Help the mayor's office resolve complaints of animal cruelty or neglect. Operate animal patrol. Organise public education campaigns on animal matters. Organise animal care programs: exhibitions, performances of dog schools, camps for children, family programs, etc.</p>
        <p>We have very good relations with the local media. Through these channels, we try to promote a humane attitude towards animals and change the terrible situation animal protection is in at present in Hungary. The mayor's office as well as people of Szekszard turn to us to help solve problems involving dogs, cats, foxes, bats, birds, all kinds of pets, etc. The foundation's work is organised and managed by chairwoman Szilvia Fiath. Day-to-day work is performed by ten people. The number of our supporting members is close to 500.</p>
        <p>Following much work of both organising and implementing, the foundation was able to open our new, more modern animal shelter on 23th June, 2000. The shelter has got all necessary certificates from the authorities and is run with the support of Szekszard Mayor's Office. The office provides the premises, buildings, and personnel for the shelter; in exchange, our foundation fulfills the duties of looking after stray animals in Szekszard. This unique co-operation stands as an example of how a common effort of city councils and civilian organisations can lead to well run shelters.</p>
        <p>In 2001, our shelter won the "Shelter of the Year" prize, awarded by the National Association of Hungarian Journalists.</p>
        <p>Our Policies<br>
        We almost always operate at our capacity which is 200 dogs, a few cats, birds, and rodents. Our shelter has always been a no-kill shelter. Our strong belief is that each dog, as a feeling creature, has a right to life, regardless of its breed and age. Every potential adopter must follow our regulations, including these points: The animal cannot be used for experiments of any kind. The animal cannot be sold to anybody. If it becomes impossible for the potential adopter to care for the animal, they agree to return it to the shelter.
        We are a non-profit organisation of very modest financial means. Our revenues consist solely of donations from private individuals and local businesses.</p>
        
